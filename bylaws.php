      <div class="col">
        <h2 class="pb-3">Article V - Duties of Officers and Members of the Board of Directors</h2>
        <h5><strong>Section 1.</strong> Office of President</h5>
        <p>
        The President shall preside at all conferences and meetings of the Oklahoma Chapter and the Board of Directors, and shall be responsible for the general supervision of the business of the Oklahoma Chapter. The President shall appoint the nominating committee and other committees the Board of Directors may deem necessary or appropriate for carrying on the activities of the Oklahoma Chapter. The President shall assign responsibilities for committee leadership to appropriate officers or members. The President may appoint individuals to positions deemed necessary for assistance in carrying out his or her duties.
        </p> 
        <h5><strong>Section 2.</strong> Office of President Elect</h5>
        <p>
        The President-Elect shall consult with and advise the President on Oklahoma Chapter matters and shall perform other duties as the President may assign. In the absence of the President or by the President's temporary incapacity to serve, the President-Elect shall exercise the duties required by Section 1 of this Article.
        </p> 
        <h5><strong>Section 3.</strong> Office of the Vice-President</h5>
        <p>
        The 1st and 2nd Vice-Presidents shall consult with and advise the President and perform other duties as the President may assign.
        </p> 
        <h5><strong>Section 4.</strong> Secretary and Treasurer</h5>
        <p>
        The Secretary shall be responsible for keeping minutes, for notifying members of meetings, and for submitting an annual report to IPMA-HR containing relevant information on Oklahoma Chapter activities and programs. The Treasurer shall be responsible for keeping the membership records and other records as well as the receipt, custody, and depositing of Oklahoma Chapter funds, subject to the control and review of the President or the Board of Directors. Only two designated Board members shall be authorized to disburse association funds. The Treasurer shall prepare and submit to the Oklahoma Chapter membership an annual financial report, and shall submit such other reports to the Board of Directors as the Board may require.

        </p> 
        <h5><strong>Section 5.</strong> Board of Directors Meetings and Duties</h5>
        <p>
        The Board of Directors shall meet at the call of the President, such meetings to be held at least quarterly. The Board of Directors shall manage the affairs of the Oklahoma Chapter in accordance with these Bylaws and any other policies established by a majority vote of the voting membership of the Oklahoma Chapter at the Annual Conference or any special meeting called by the Board of Directors. The Board of Directors is authorized to make interim policy decisions in the name of the Oklahoma Chapter. Such policies shall be subject to review and possible revision at the next Annual Conference. Members of the Board of Directors shall review the activities of, develop plans for future activities, and consider other matters brought to the attention of the Board by officers or members.
        </p> 
        <h5><strong>Section 6.</strong> Attendance and Participation of Board Members</h5>
        <p>
        Board members shall attend all scheduled board meetings, actively assist in the planning and implementation of board and Oklahoma Chapter initiatives, actively participate in all Oklahoma Chapter activities, and attend all Oklahoma Chapter sponsored conferences or training sessions. Failure of a board member to attend two consecutive board meetings or to actively participate in Oklahoma Chapter activities, conferences and training, shall be cause for a letter of concern issued by the President. Upon further concern, the situation should be discussed by board members to determine any further appropriate action, possibly to include requesting the resignation of the individual from the Oklahoma Chapter Board.
        </p> 
      </div>
    </div>
  </div>
