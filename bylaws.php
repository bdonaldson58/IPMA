    <div class="row">
      <div class="col">
        <h2 class="pb-3">Article VII - Amendments to Bylaws</h2>
        <h5><strong>Section 1.</strong></h5>
        <p>
        Proposed amendments to these Bylaws may be initiated by action of the Board of Directors or upon written petition signed by at least ten (10) Oklahoma Chapter members. Proposed amendments to the Bylaws must be submitted in writing to the President at least 20 calendar days prior to the date they are to be considered by the Board of Directors. Amendments initiated by petition shall be addressed to the President for submission to the membership.
        </p> 
        <h5><strong>Section 2.</strong></h5>
        <p>
        Proposed amendments shall be transmitted by the Secretary to the members in writing at least 30 calendar days in advance of the date on which they are to be voted on by the members. Proposed amendments initiated by petition shall be accompanied by the recommendation of the Board of Directors.
        </p> 
        <h5><strong>Section 3.</strong></h5>
        <p>
        Before any amendments to these Bylaws are formally presented to the membership for consideration, the Secretary shall submit the proposed amendment(s) to IPMA-HR with a request that the proposed Bylaws change(s) be reviewed for conformity with policies of IPMA. No final action on these proposed amendments will be taken until a response to a request for review has been received by the Oklahoma Chapter.
        </p> 
        <h5><strong>Section 4.</strong></h5>
        <p>
        Any amendments to these Bylaws deemed to be in conflict with the objectives or policies of IPMA-HR shall be referred back to the board or submitting parties, as appropriate.
        </p> 
        <h5><strong>Section 5.</strong></h5>
        <p>
        Any provisions of these Bylaws may be amended, subject to IPMA-HR concurrence, by a majority of votes cast by the Oklahoma Chapter membership at a meeting of the association. Any provision of these Bylaws may also be amended by unanimous vote of the Board of Directors, subject to IPMA-HR concurrence; however, any such amendment shall be considered temporary and shall also be subject to required approval of the membership by majority vote in order to remain in force and effect.
        </p> 
