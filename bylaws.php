        <h2 class="pb-3">Article VI - Training Conferences and Meetings</h2>
        <h5><strong>Section 1.</strong> Training</h5>
        <p>
        Four regular meetings of the Oklahoma Chapter shall be held annually. These meetings may take the form of training conferences, with the program content, costs, location, and dates to be determined by the Board of Directors. Training announcements shall be sent to members at least two weeks before the meeting date.
        </p> 
        <h5><strong>Section 2.</strong> Business Meetings</h5>
        <p>     
        Annually, a conference shall include a business meeting during which members will conduct business, including but not limited to the election of officers. If business is to be conducted at any training conference, the conference announcement shall include notification of the upcoming business session. At any meeting held for the purpose of transacting Oklahoma Chapter business, a quorum shall consist of at least fifteen (15) members.
        </p> 
        <h5><strong>Section 3.</strong> Special Business Meetings</h5>
        <p>
        The President or the Board of Directors may call special meetings of the general membership at any time. Special meetings may also be called when forty (40) percent of the voting members indicate a desire to have a meeting, such notice to include the general nature of the business to be conducted.
        </p> 
        <h5><strong>Section 4.</strong> Conducting Oklahoma Chapter Proceedings</h5>
        <p>
        Except as otherwise provided by these Bylaws, all Oklahoma Chapter proceedings shall be governed by the latest revised edition of Roberts' Rules of Order.
        </p> 

