<!-- ###### Bylaws Section 2 #######-->
<section id="about-section-2" class="pt-1 pb-4">
<div class="container">
<hr class="bylaws-separator">
  <div class="row">
    <div class="col">
      <h2 class="pb-3">Article II — Objectives</h2>
      <h5>The objectives of the Oklahoma Chapter include:</h5>
      <ul> 
        <li>Providing professional support and a forum for persons engaged in public human resources (HR) administration and/or employee/labor relations to discuss current issues within member jurisdictions and to provide a medium for mutual self-improvement.</li>
        <li>Implementing the objectives and programs of IPMA-HR within the state of Oklahoma.</li>
        <li>Explaining and interpreting the objectives and methods of HR administration to the general public, civic groups, government officials and employees.</li>
        <li>Encouraging and facilitating cooperative action among public jurisdictions and private employers within the state of Oklahoma on HR issues of mutual concern.</li>
        <li>Improving the quality of HR administration in public jurisdictions within the state of Oklahoma.</li>
        <li>Analyzing, reviewing, and providing input on legislative matters dealing with HR administration in public organizations.</li>
        <li>Disseminating and exchanging information, data, and analysis of policy pertaining to all areas of employee/labor relations.</li>
        <li>Fostering communication and cooperation among members to promote sound public policies, practices, and legislation.</li>
        <li>Promoting innovative solutions to issues in Oklahoma public sector labor-management relations.</li>
        <li>Providing a voice for Oklahoma public sector management and human relations professionals in the development of state and national policy.</li>
        <li>Providing training and continuing education opportunities.</li>
      </ul>
  </div>
</div>
</section>

<!-- ###### Bylaws Section 3 #######-->
<section id="about-section-2" class="pt-1 pb-4">
<div class="container">
<hr class="bylaws-separator">
  <div class="row">
    <div class="col">
      <h2 class="pb-3">Article III - Types of Membership; Dues</h2>
      <h5><strong>Section 1.</strong> General Information</h5>
      <ul>
        <li>All applications for membership shall include a certification in writing that the applicant qualifies under the conditions for membership specified herein.</li>
        <li>No membership shall be transferable (organizational memberships including multiple unnamed participants shall not be considered transferred memberships).</li>
        <li>Members changing positions may retain their membership for a reasonable period of time, provided all other obligations of membership are met and the member is not employed in a position contrary to the goals and objectives of the Oklahoma Chapter, and the member is seeking a position which would permit the member to retain membership status.</li>
      </ul>
      <p>Eligibility for membership or continuation of membership shall be determined by action of the Board of Directors.</p>

      <h5><strong>Section 2.</strong> Types of Membership</h5>
      <ul>
      <li>Individual Membership is available to any person who is either an official or staff member engaged in HR-related work in a public sector entity solely on behalf of management, or engaged in teaching human resources administration as a faculty member of a college or university. Individual members are eligible to vote and/or hold office.</li>
      <li>Affiliate / Sponsor Membership is available to any other person interested in improving HR management and not eligible for individual membership. Members must be involved in working solely for management pursuits related to the fields of HR or employee/labor relations. Affiliate/Sponsor members are not eligible to vote or hold office.</li>
      <li>Student Membership is available to any full-time graduate or undergraduate student, or any person participating in a formal public service internship sponsored by a recognized academic institution. Student members are not eligible to vote or hold office.</li>
      <li>Organizational Membership is available for a specified number of participants employed by a public entity. For an individual to be considered an Oklahoma Chapter member by way of an organizational membership, he or she must be named by the organization as a participant. Such members are eligible to vote and/or hold office.</li>
      <li>Honorary Membership may be granted in recognition of outstanding support and service on behalf of the Oklahoma Chapter as follows:</li>
      <li>Past Presidents ~ upon retirement from HR or employee/labor relations field, or</li>
      <li>By a unanimous vote of the Oklahoma Chapter Board of Directors, to any person who has made significant contributions to the objectives of the Oklahoma Chapter, or who has served the Oklahoma Chapter with distinction.</li>
    </ul>
    <p>Honorary members shall not be required to pay membership dues, but shall have all other privileges of membership in the Oklahoma Chapter, except the right to vote or hold office.</p>

    <h5><strong>Section 3.</strong> Dues</h5>
      <ul>
        <li>The annual Oklahoma Chapter membership dues shall be established by a majority vote of the Board of Directors.</li>
        <li>Dues may vary for membership categories.</li>
        <li>The Board may change the annual dues, subject to confirmation at the next annual conference.</li>
        <li>Dues and other income shall be used to pay the expenses of conducting the business of the Oklahoma Chapter.</li>
      </ul>
    </div>
  </div>
</div>
