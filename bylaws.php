<!-- ###### Bylaws Section 4 #######-->
<section id="about-section-2" class="pt-1 pb-4">
<div class="container">
<hr class="bylaws-separator">
  <div class="row">
    <div class="col">
      <h2 class="pb-3">Article IV - Officers and Board Directors; Meetings</h2>
      <h5><strong>Section 1.</strong> Officers</h5>
      <ul>
        <li>The Oklahoma Chapter officers shall consist of a President, President-Elect, two Vice-Presidents, a Secretary and a Treasurer.</li>
        <li>The President shall be an individual member or a designated agency member representative of IPMA-HR.</li>
        <li>The Oklahoma Chapter Board of Directors may authorize the expenditure of funds to purchase an individual membership of IPMA-HR for the individual serving as President and President Elect.</li>
        <li>Each officer shall be a member of the Oklahoma Chapter. Any officer who becomes ineligible for Oklahoma Chapter membership shall cease to be an officer.</li>
      </ul>

      <h5><strong>Section 2.</strong> Board of Directors</h5>
      <ul>
      <li>Except as otherwise provided by these Bylaws, the Board of Directors shall consist of the six officers, the Immediate Past President, and any other Past President appointed by the President and approved by the Board.</li>
      <li>Such Past President shall serve until the expiration of the appointing President's term.</li>
      <li>A majority of the Board of Directors shall constitute a quorum of that body. If at any meeting of the Board there is less than a quorum, any member present may adjourn the meeting and no official actions or expenditures may be effected without said quorum. However, any action of the Board may be taken without a meeting if consent in writing, including email, setting forth the action taken, is approved by a quorum of the Board entitled to vote with respect to the subject matter thereof.</li>
   </ul>

    <h5><strong>Section 3.</strong> Terms of Office</h5>
      <p>The term of office of all Oklahoma Chapter officers and other members of the Board of Directors, except the Secretary and the Treasurer, shall be for one year, from July 1 through June 30.  The term of office for the Secretary and the Treasurer shall be for two years, from July 1 through June 30.</p>
    
      <h5><strong>Section 4.</strong> Succession / Nomination / Election of Officers</h5>
      <ul>
      <li>The offices of President, President-Elect, and 1st Vice-President shall be filled by succession.</li>
      <li>The Board of Directors shall, by majority vote, fill any Board vacancy, except that of the Immediate Past President.</li>
      <li>Each year the President shall appoint a Nominating Committee consisting of five members representative of the dues-paying membership.</li>
      <li>The committee shall receive and prepare nominations for any vacant officer positions and present such nominations for membership vote.</li>
      <li>Additional nominations for any elected position may be made from the floor, provided that the individual making the nomination has consent of the individual being nominated.</li>
      <li>All nominees must be willing to support and comply with the Oklahoma Chapter bylaws if elected.</li>
      <li>Voting shall be by voice, sign or by ballot, as the members may determine.</li>
   </ul>
    </div>
  </div>
